export function getProductRatingInfo(productId) {
  const reviews = productsReviews.filter((r) => r.productId === productId);

  if (reviews.length === 0) {
    return {
      average: 0,
      count: 0,
    };
  }

  const total = reviews.reduce((sum, r) => sum + r.rating, 0);
  const avg = (total / reviews.length).toFixed(1);

  return {
    average: avg,
    count: reviews.length,
  };
}

